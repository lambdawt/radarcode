% 论文中的理论雷达威力计算
Gt = 36;  % 发射增益，dB 
Gr = 36;  % 接收增益，dB
Pt = 40e3;  % 发射峰值功率，w
f = 5e9;  % 工作频率  
lambda = 3e8 / f;  % 发射信号波长
sigma = 5;  % 目标平均RCS
Tr = 256e-6;  % 脉宽
k = 1.38e-23; % 玻尔兹曼常数
T= 290;  % 开尔文温度，K
Fn = 3.5;  % 噪声系数
Lr = 0.017 * 192;  % 大气损耗
Ls = 5.5;  % 系统损耗
SNRd = 12;  % 最小可检测信噪比

Rmax = (Pt * 10^((Gt+Gr)/10) * lambda^2 * sigma * Tr / (4*pi)^3 / k / T / (10^((Fn+Lr+Ls+SNRd)/10)))^0.25


