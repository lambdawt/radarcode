function [ WindV ] = paraset_windvelocity( vx,vy )
%========================================================================%
%功能：风速参数设置                                              %
%输入：
%   vx            x轴方向的速度分量
%   vy            y轴方向的速度分量

%输出：
%   WindV           风速参数结构体
%========================================================================%
WindV.vx=vx;
WindV.vy=vy;

end

